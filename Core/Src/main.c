void set_prescaler(uint16_t freq_hz)
{
  uint16_t psc = (uint16_t)((16000000UL / freq_hz / 100UL) - 1UL);
  __HAL_TIM_SET_PRESCALER(&htim1, psc);
  HAL_TIM_GenerateEvent(&htim1, TIM_EVENTSOURCE_UPDATE);
  __HAL_TIM_SET_PRESCALER(&htim2, psc);
  HAL_TIM_GenerateEvent(&htim2, TIM_OPMODE_REPETITIVE);
}

void set_tim_period(uint8_t freq_hz, uint32_t us) {
    uint32_t period_us_scaled = us / 10;           
    uint32_t freq_period_us = 1000000UL / freq_hz;
    uint32_t freq_period_scaled = freq_period_us / 10;
    
    uint16_t psc1 = 159;  
    
    uint32_t arr;
    arr = freq_period_scaled - 1;
    
    uint32_t pulse_scaled = period_us_scaled;
    if (pulse_scaled > arr) pulse_scaled = arr;
    
    __HAL_TIM_SET_PRESCALER(&htim1, psc1);
    __HAL_TIM_SET_AUTORELOAD(&htim1, (uint16_t)arr);
    __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, (uint16_t)pulse_scaled);
    HAL_TIM_GenerateEvent(&htim1, TIM_EVENTSOURCE_UPDATE);
    
    uint16_t psc2 = (uint16_t)((16000000UL / freq_hz / 100UL) - 1UL);
    __HAL_TIM_SET_PRESCALER(&htim2, psc2);
    HAL_TIM_GenerateEvent(&htim2, TIM_OPMODE_REPETITIVE);
}

void set_pwm(uint8_t pwm){
  __HAL_TIM_SET_AUTORELOAD(&htim1, 99);
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, pwm);
  HAL_TIM_GenerateEvent(&htim1, TIM_EVENTSOURCE_UPDATE);
}

uint8_t parser(const char *str)
{
  int freq_tmp = 0;
  int pwm_tmp = 0;
  int parced_args = sscanf(str, "%7s %d %d", laser_cmd.sComm, &freq_tmp, &pwm_tmp);

  if (parced_args != 3 && strcmp(laser_cmd.sComm, "fullGaz") != 0){
    incorrect_input();
    return 0;
  }
  else if (strcmp(laser_cmd.sComm, "fullGaz") == 0){
    laser_cmd.pwm = 100;
    laser_cmd.freq = 100;
    return 1;
  }

  else if (strcmp(laser_cmd.sComm, "freqCUs") == 0){
    if (freq_tmp < 5 || freq_tmp > 100){
      incorrect_input();
      return 0;
    }
    else if(pwm_tmp < 0 || (pwm_tmp > 1000000UL / freq_tmp)){
      incorrect_input();
      return 0;
    }
    strcpy(laser_cmd_us.sComm, laser_cmd.sComm);
    laser_cmd_us.us = (uint32_t)pwm_tmp;
    laser_cmd_us.freq = (uint8_t)freq_tmp;
    return 2;
  }

  else if (strcmp(laser_cmd.sComm, "freqCmd") != 0){
    incorrect_input();
    return 0;
  }
  else{
    if (freq_tmp < 0 || freq_tmp > 65536){
      incorrect_input();
      return 0;
    }
    else if(pwm_tmp < 0 || pwm_tmp > 65536){
      incorrect_input();
      return 0;
    }
    laser_cmd.pwm = (uint8_t)pwm_tmp;
    laser_cmd.freq = (uint16_t)freq_tmp;
    return 1;
  }
}



